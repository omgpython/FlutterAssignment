void main(){
  int no1 = 10;
  int no2 = 5;

  //addition
  print("The sum of $no1 and $no2 is ${no1+no2}");

  //Subtraction
  print("The subtraction of $no1 and $no2 is ${no1-no2}");

  //Multiply
  print("The multiply of $no1 and $no2 is ${no1*no2}");

  //Division
  print("The division of $no1 and $no2 is ${no1/no2}");

}